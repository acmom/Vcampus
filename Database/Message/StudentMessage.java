package Message;

public class StudentMessage {
	private String student_id;
	private String student_name;
	private String student_onecard;
	private String student_grade;
	private String student_course;
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getStudent_onecard() {
		return student_onecard;
	}
	public void setStudent_onecard(String student_onecard) {
		this.student_onecard = student_onecard;
	}
	public String getStudent_grade() {
		return student_grade;
	}
	public void setStudent_grade(String student_grade) {
		this.student_grade = student_grade;
	}
	public String getStudent_course() {
		return student_course;
	}
	public void setStudent_course(String student_course) {
		this.student_course = student_course;
	}
}
